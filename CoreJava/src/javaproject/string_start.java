package javaproject;

import java.util.Scanner;

public class string_start {
	
	public static void main(String[] args) {
		
		String str="RED IS FAVORITE COLOUR:";
		System.out.println(str);
		System.out.println("STRING START WITH RED ?" +str.startsWith("RED"));
		
		String s="ORANGE IS ALSO MY FAVORITE COLOUR:";
		System.out.println(s);
		System.out.println("STRING START WITH RED ?" +s.startsWith("RED"));
		
		
		
		
	}

}
